# Create a function that takes a string as its argument and returns the string
# in reversed order.

def reverse(txt):
    return txt[::-1]



print(reverse("Hello World")) # "dlroW olleH"

print(reverse("The quick brown fox.")) # ".xof nworb kciuq ehT"

print(reverse("Edabit is really helpful!")) # "!lufpleh yllaer si tibadE"
